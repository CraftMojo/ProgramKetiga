package net.craftmojo.programku;

public class PersegiPanjang {
    private int p;
    private int l;

    public int luasPersegiPanjang() {
        //proses
        return this.p * this.l;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getL() {
        return l;
    }

    public void setL(int l) {
        this.l = l;
    }
}
