package net.craftmojo.programku;

public class Segitiga {
    private int a;
    private int t;

    public int luasSegitiga() {
        //proses
        return this.a * this.t;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }
}
